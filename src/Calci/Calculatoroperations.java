package Calci;

public class Calculatoroperations {
			public int addition(int a,int b)
			{
				return a+b;
			}
			public int substraction(int a,int b)
			{
				return a-b;
			}
			public int multiply(int a,int b)
			{
				return a*b;
			}
			public float divide(int a,int b)
			{
				return a/b;
			}
			public int modulus(int a,int b)
			{
				return a%b;
			}
	}

